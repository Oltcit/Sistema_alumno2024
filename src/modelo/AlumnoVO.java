package modelo;

public class AlumnoVO {
	private int dni;
	private String ape;
	private String fnac;
	private byte doc;
	public int getDni() {
		return dni;
	}
	public void setDni(int dni) {
		this.dni = dni;
	}
	public String getApe() {
		return ape;
	}
	public void setApe(String ape) {
		this.ape = ape;
	}
	public String getFnac() {
		return fnac;
	}
	public void setFnac(String fnac) {
		this.fnac = fnac;
	}
	public byte getDoc() {
		return doc;
	}
	public void setDoc(byte doc) {
		this.doc = doc;
	}
	
	
}
