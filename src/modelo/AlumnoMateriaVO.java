package modelo;

public class AlumnoMateriaVO {
	private int idam;
	private int dni;
	private String codmat;
	private int nota;
	
	public int getIdam() {
		return idam;
	}
	public void setIdam(int idam) {
		this.idam = idam;
	}
	public int getDni() {
		return dni;
	}
	public void setDni(int dni) {
		this.dni = dni;
	}
	public String getCodmat() {
		return codmat;
	}
	public void setCodmat(String codmat) {
		this.codmat = codmat;
	}
	public int getNota() {
		return nota;
	}
	public void setNota(int nota) {
		this.nota = nota;
	}
	
	
	
}
